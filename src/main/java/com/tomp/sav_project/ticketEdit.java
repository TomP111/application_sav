    public ticketEdit(int IdTicket, int idClient) {
        initComponents();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/ppe?characterEncoding=utf8", "root", "");

            Statement statement = connection.createStatement();
            ResultSet resultSet = statement.executeQuery("SELECT * FROM tickets WHERE ID_Ticket = '" + IdTicket + "'");
            while (resultSet.next()) {
                ticketDesc = resultSet.getString("Description");
                ticketState = resultSet.getString("Etat");
                ticketType = resultSet.getString("Type");
                ticketDate = resultSet.getString("Date");
            }

            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        TicketID = IdTicket;
        clientID = idClient;
        

        TypeFill.setText(ticketType);
        StateFill.setText(ticketState);
        DateFill.setText(ticketDate);
        DescArea.setText(ticketDesc);
        repaint();

    }

